package programming;

public class loop {

	public static void main(String[] args) {
		int i=1;
		while(i<6)
		{
			System.out.println(i);
			i++;
		}
		i=1;
		do {
			System.out.println(i);
			i++;
		} while (i<6);
		for(int j=1;j<6;j++)
		{
			System.out.println(j);
		}
		
	}

}
