package programming;

public class loopReverse {

	public static void main(String[] args) {
		int i=5;
		while(i>0)
		{
			System.out.println(i);
			i--;
		}
		i=5;
		do {
			System.out.println(i);
			i--;
		} while (i>0);
		for(int j=5;j>0;j--)
		{
			System.out.println(j);
		}
		
	}

}
