class Method5 //addition using parameterized method
{
	public static void add(int a,int b)//called method 
	{
		int c=a+b;
		System.out.println(c);
	}
	public static void main(String[] args) // calling method
	{
		System.out.println("Formal Arguments, Actual Arguments and Parameterized methods");
		add(20,50);//actual arguments, method calling statement
	}
}
