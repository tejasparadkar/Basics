class Method6 //multiplication using parameterized method
{
	public static void multiply(int a,int b, int c, int d,int e)
	{
		int f = a*b*c*d*e;
		System.out.println(f);
	}
	public static void main(String[] args) 
	{
		System.out.print("Multiplication is ");
		multiply(1,2,3,4,5);
	}
}
